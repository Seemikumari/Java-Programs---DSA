public class ArrayPWS {
    public static void main(String[] args) {
        //declare an array in java
      int [] studentsScore = { 85, 95 , 80, 70, 75};
      //display all elements in an array
        // access the elements inside an array using an index number
      for(int i = 0; i<studentsScore.length; i++){
          System.out.print(studentsScore[i]+" ");
      }
    }
}
